@file:JvmName(name = "Constants")

package it.czerwinski.android.graphics

/** A `Float` value equal to pi constant. */
const val PI: Float = Math.PI.toFloat()

/** A `Float` value equal to double pi constant. */
const val DOUBLE_PI: Float = PI * 2f

/** A `Float` value equal to right angle in measured in degrees. */
const val RIGHT_ANGLE: Float = 90f

/** A `Float` value equal to straight angle in measured in degrees. */
const val STRAIGHT_ANGLE: Float = 180f

/** A `Float` value equal to full angle in measured in degrees. */
const val FULL_ANGLE: Float = 360f
